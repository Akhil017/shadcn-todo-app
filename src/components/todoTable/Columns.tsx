"use client";

import { ColumnDef } from "@tanstack/react-table";
import { TodoType } from "./data/schema";
import { TodoTableHeader } from "./TodoTableHeader";
import { TodoTableRowActions } from "./TodoTableRowActions";
import { Badge } from "../ui/badge";
import { labels, priorities, statuses } from "./data/Data";

export const columns: ColumnDef<TodoType>[] = [
  {
    accessorKey: "id",
    header: ({ column }) => <TodoTableHeader column={column} title="Task" />,
    cell: ({ row }) => <div className="w-[80px]">{row.getValue("id")}</div>,
    enableSorting: false,
    enableHiding: false,
  },
  {
    accessorKey: "todo",
    header: ({ column }) => <TodoTableHeader column={column} title="Title" />,
    cell: ({ row }) => {
      // const label = labels.find((label) => label.value === row.original.label);
      const label = { label: "dummy label" };

      return (
        <div className="flex space-x-2">
          {label && <Badge variant="outline">{label.label}</Badge>}
          <span className="max-w-[500px] truncate font-medium">
            {row.getValue("title")}
          </span>
        </div>
      );
    },
  },
  {
    accessorKey: "status",
    header: ({ column }) => <TodoTableHeader column={column} title="Status" />,
    cell: ({ row }) => {
      const status = statuses.find(
        (status) => status.value === row.getValue("status")
      );

      if (!status) {
        return null;
      }

      return (
        <div className="flex w-[100px] items-center">
          {status.icon && (
            <status.icon className="mr-2 h-4 w-4 text-muted-foreground" />
          )}
          <span>{status.label}</span>
        </div>
      );
    },
    filterFn: (row, id, value) => {
      return value.includes(row.getValue(id));
    },
  },
  {
    accessorKey: "priority",
    header: ({ column }) => (
      <TodoTableHeader column={column} title="Priority" />
    ),
    cell: ({ row }) => {
      const priority = priorities.find(
        (priority) => priority.value === row.getValue("priority")
      );

      if (!priority) {
        return null;
      }

      return (
        <div className="flex items-center">
          {priority.icon && (
            <priority.icon className="mr-2 h-4 w-4 text-muted-foreground" />
          )}
          <span>{priority.label}</span>
        </div>
      );
    },
    filterFn: (row, id, value) => {
      return value.includes(row.getValue(id));
    },
  },
  {
    id: "actions",
    cell: ({ row }) => <TodoTableRowActions row={row} />,
  },
];
